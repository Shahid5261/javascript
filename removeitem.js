// to remove the series of elements  of array


var array = [1,2,3,4,5,6];
array.shift(); //here we use shift() method to remove the ist element
console.log(array);

//to remove the last element of array

var array = [1,2,3,4,5,6];
array.pop();  //here we use the pop() method to remove the last element 
console.log(array);

// to remove the series of elements of an array 
var array = [1,2,3,4,5,6];
array.splice(2,3); //here we use the splice()method to remove the series of elements 
console.log(array);